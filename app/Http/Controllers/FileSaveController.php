    /**
     * Store a newly created resource in storage.
     *
     * @param  App\Http\Requests\FileSaveRequest $request
     * @return \Illuminate\Http\Response
     */
    public function store(FileSaveRequest $request)
    {
        $file = $request->file;

        try{
            if(Storage::put('image',$file)):
                return redirect()->back()->with('file-status', 'Imagem salva com sucesso!');
            endif;
        }catch(\Exception $e){
            return redirect()->back()->with('file-status', $e->getMessage());
        }
    }

